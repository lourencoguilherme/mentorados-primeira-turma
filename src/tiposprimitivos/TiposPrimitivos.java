package tiposprimitivos;

import java.util.Arrays;

public class TiposPrimitivos {
    public static void main(String[] args) {
        int[] array = {1,1,904,46567};

        System.out.println(array);

        String variavelString = "Ola Mundo agora numa variavel";

        System.out.println(variavelString);

        int variavelInteira = 1111111111;

        System.out.println(variavelInteira);

        long variavelLong = 3000000000000000000l;

        System.out.println(variavelLong);


        float variavelFloat = 12.23f;

        System.out.println(variavelFloat);

        double variavelDouble = 34.5855555555555555555555555555555555555566666;

        System.out.println(variavelDouble);

        boolean variavelBoolean = true;

        System.out.println(variavelBoolean);

        boolean variavelBooleanFalse = false;

        System.out.println(variavelBooleanFalse);

        int numero1 = 30;

        int numero2 = -27;

        int soma = numero1 + numero2;

        System.out.println(soma);

        String ola = "Ola";
        String mundo = "Mundo";

        System.out.println(ola + mundo);

    }
}
